
# 3. Write a program that takes two friends age and show the total of their age.

# Getting the two values of two friends age
friend1_age = int(input('Enter Friend 1 Age:'))
friend2_age = int(input('Enter Friend 2 Age:'))
# Adding their ages
total_age = friend1_age + friend2_age
# Displaying their total age
print('Both of your Friends Total Age is: ', total_age)